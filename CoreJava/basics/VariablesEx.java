package basics;

public class VariablesEx {
	
	
	
	
	public static void main(String[] args) {
		
		byte value1 = 127;			//local variable
		
		short value2 = 10002;
		
		int value = 2323234;
		
		long value3 = 987654321;
		
		float pi = 3.14f ;
		
		boolean value4 = true;
		
		char name = 'q';
		
		System.out.println(value1 +"\t"+value2+"\t"+value+"\t"+value3+"\t"+pi+"\t"+value4+"\t"+name);
		
		System.out.println(pi);
		
	}

}
